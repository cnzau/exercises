package car;

public class Car {
	int noOfWheels;
	int noOfDoors;
	int carSeats;
	String color;
	String transmission;
	
	public Car(int wheels,	int doors,	int carSeats,	String color,	String transmission) {
		this.noOfWheels = wheels;
		this.noOfDoors = doors;
		this.carSeats = carSeats;
		this.color = color;
		this.transmission = transmission;
	}
}
